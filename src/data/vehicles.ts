export function getVehicleById(id: string): Vehicle | undefined {
  return vehicles.find(v => v.id === id)
}

export function getVehicleBySlug(slug: string): Vehicle | undefined {
  return vehicles.find(v => v.slug === slug)
}

export function getFeaturedVehicles(): Vehicle[] {
  return vehicles.filter(v => v.featured && v.status !== 'sold')
}

export function getNewArrivals(count = 4): Vehicle[] {
  return [...vehicles]
    .filter(v => v.status !== 'sold')
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, count)
}

export function getSimilarVehicles(vehicle: Vehicle, count = 3): Vehicle[] {
  return vehicles
    .filter(v => v.id !== vehicle.id && v.status !== 'sold')
    .filter(v => v.make === vehicle.make || v.bodyType === vehicle.bodyType)
    .slice(0, count)
}

export function getUniqueMakes(): string[] {
  return [...new Set(vehicles.map(v => v.make))].sort()
}

export function getModelsByMake(make: string): string[] {
  return [...new Set(vehicles.filter(v => v.make === make).map(v => v.model))].sort()
}
